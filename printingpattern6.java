                        //HALF PYRAMID BY USING NUMBERS//

public class printingpattern6 {
    public static void main(String[] args) {
        int n=6;
        for(int i =1; i<=n; i++){
            for(int j=1;j<=i;j++){
                System.out.print(j);
            }
            System.out.println();
        }
    }
    
}



